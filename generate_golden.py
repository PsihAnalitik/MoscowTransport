def generate_excel(output_path: str):
    wb = Workbook()
    ws = wb.active
    ws.title = "Golden Dataset"

    # ── Стили ────────────────────────────────────────────────
    header_font = Font(name="Calibri", bold=True, size=11, color="FFFFFF")
    header_fill = PatternFill(start_color="4472C4", end_color="4472C4", fill_type="solid")
    header_align = Alignment(horizontal="center", vertical="center", wrap_text=True)

    # Заливки для категорий
    cat_fills = {
        "sales":    PatternFill(start_color="E2EFDA", end_color="E2EFDA", fill_type="solid"),
        "trips":    PatternFill(start_color="DAEEF3", end_color="DAEEF3", fill_type="solid"),
        "price":    PatternFill(start_color="FDE9D9", end_color="FDE9D9", fill_type="solid"),
        "multi":    PatternFill(start_color="E4DFEC", end_color="E4DFEC", fill_type="solid"),
        "no_date":  PatternFill(start_color="F2F2F2", end_color="F2F2F2", fill_type="solid"),
    }
    thin_border = Border(
        left=Side(style="thin"), right=Side(style="thin"),
        top=Side(style="thin"), bottom=Side(style="thin"),
    )
    wrap_align = Alignment(vertical="top", wrap_text=True)

    # ── Заголовки ────────────────────────────────────────────
    headers = ["№", "question", "parse_task_type", "parse_date", "model_answer", "answer"]
    for col_idx, header in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col_idx, value=header)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = header_align
        cell.border = thin_border

    # ── Данные ───────────────────────────────────────────────
    for row_idx, (question, series, dates, answer) in enumerate(GOLDEN_DATA, 2):
        num = row_idx - 1

        # Определяем категорию для заливки
        if "," in series:
            fill = cat_fills["multi"]
        elif dates == "null":
            fill = cat_fills["no_date"]
        else:
            fill = cat_fills.get(series, PatternFill())

        values = [num, question, series, dates, "", answer]
        for col_idx, val in enumerate(values, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=val)
            cell.border = thin_border
            cell.alignment = wrap_align
            cell.fill = fill

    # ── Ширина столбцов ──────────────────────────────────────
    ws.column_dimensions["A"].width = 5     # №
    ws.column_dimensions["B"].width = 65    # question
    ws.column_dimensions["C"].width = 22    # parse_task_type
    ws.column_dimensions["D"].width = 32    # parse_date
    ws.column_dimensions["E"].width = 45    # model_answer
    ws.column_dimensions["F"].width = 18    # answer

    # ── Закрепляем шапку ─────────────────────────────────────
    ws.freeze_panes = "A2"

    # ── Авто-фильтр ─────────────────────────────────────────
    ws.auto_filter.ref = f"A1:F{len(GOLDEN_DATA) + 1}"

    wb.save(output_path)
    print(f"✅ Эталонная таблица сохранена: {output_path}")
    print(f"   Вопросов: {len(GOLDEN_DATA)}")


if __name__ == "__main__":
    ROOT = os.path.dirname(os.path.abspath(__file__))
    output = os.path.join(ROOT, "tests", "golden_dataset.xlsx")
    os.makedirs(os.path.dirname(output), exist_ok=True)
    generate_excel(output)
